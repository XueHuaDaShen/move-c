// 按时间排序
const sortByTime = (a, b) => {
  // 1, 降序; 0, 升序
  let type = 1;
  if(type === 1) {
    return new Date(b.time) - new Date(a.time)
  }
  if(type === 0) {
    return new Date(a.time) - new Date(b.time)
  }
}
export default newsList.sort(sortByTime);